void	print_token_list(t_tkn_lst *lst)
{
	t_tkn_nd	*nd_ptr;

	printf("\ntoken list info | tkn cnt : %zu | \n", lst->tkn_cnt_);
	nd_ptr = lst->head_;
	while (nd_ptr != NULL)
	{
		printf("[%s] - type : %d\n", nd_ptr->val_, nd_ptr->type_);
		nd_ptr = nd_ptr->next_;
	}
	printf("\n\n");
}

