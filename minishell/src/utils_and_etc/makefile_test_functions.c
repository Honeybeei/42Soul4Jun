/**
 * @brief Say hello n times. 
 * 
 * @param n 
 */
void	say_hello(int n)
{
	int	idx;

	idx = 0;
	while (idx < n)
	{
		printf("Hello world!!\n");
		idx++;
	}
	return ;
}

